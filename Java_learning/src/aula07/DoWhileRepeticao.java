package aula07;

public class DoWhileRepeticao {
	public static void main(String[] args) { // O DoWHile executa o bloco para depois verificar a codição, logo ele executa sempre o codigo 1 vez pelo menos.
		int i = 0;
		do { // FAÇA ISSO PRIMEIRO
			i ++;
			System.out.println("Produto" + i);
			
		} while ( i < 3); // VERIFIQUE DEPOIS
		
		}
		
	}


